#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (str[i] != '\0' && s[i] != (char)c)
		i++;
	if (str[i] == (char)c)
		return ((char *)(s + i));
	return (NULL);
}
